    private void tinhdiemtb() {
        try {
            float DiemJava = Float.parseFloat(txtDiemJava.getText());
            float DiemSQL = Float.parseFloat(txtDiemSQL.getText());
            float diemtb = ( DiemJava + DiemSQL ) / 2;
            String abc = String.valueOf(diemtb);
            txtDiemTB.setText(abc);
            
        } catch (Exception ex) {
            if (txtDiemJava.getText().equals("") || txtDiemSQL.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Không được bỏ trống điểm");
            } else {
                JOptionPane.showMessageDialog(null, "Nhập sai giá trị điểm");
            }
        }
    }
    private void loadData() {

        try {
            Connect a = new Connect();
            Connection con = a.getConnection();
            String sql = "SELECT * FROM BANGDIEM";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData meta = rs.getMetaData();
            int n = meta.getColumnCount();
            Vector col = new Vector();
            col.add("Mã sinh viên");
            col.add("Điểm Java");
            col.add("Điểm SQL");
            col.add("Điểm trung bình");
            dftb.setColumnIdentifiers(col);
            while (rs.next()) {
                Vector row = new Vector();
                for (int i = 0; i < n; i++) {
                    if (i < n) {
                        row.addElement(rs.getString(i + 1));
                    } else {
                    }
                }
                dftb.addRow(row);

            }
            tblSV.setModel(dftb);
            tblSV.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (tblSV.getSelectedRow() >= 0) {
                        txtMaSV.setText(tblSV.getValueAt(tblSV.getSelectedRow(), 0).toString());
                        txtDiemJava.setText(tblSV.getValueAt(tblSV.getSelectedRow(), 1).toString());
                        txtDiemSQL.setText(tblSV.getValueAt(tblSV.getSelectedRow(), 2).toString());
                        tinhdiemtb();
                    }
                }
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Lỗi : " + ex);
            Logger.getLogger(Connection.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


    private void btLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLuuActionPerformed
        // TODO add your handling code here:
        Connect a = new Connect();    
            if (checkTT==1)
            {
                try {
                    Connection con = a.getConnection();
                    String sql = "INSERT INTO BANGDIEM values(?,?,?,?)";
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setString(1, txtMaSV.getText());
                    ps.setString(2, txtDiemJava.getText());
                    ps.setString(3, txtDiemSQL.getText());
                    tinhdiemtb();
                    ps.setString(4, txtDiemTB.getText());
                    ps.executeUpdate();
                    dftb.setRowCount(0);
                    loadData();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "\tKhông nhập được điểm\nNgười này chưa tồn tại hoặc đã có điểm");
                    Logger.getLogger(Connection.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else if( checkTT==2)
            {
                try {
                    Connection con = a.getConnection();
                    String sql = "UPDATE BANGDIEM SET DIEMJAVA = ?,DIEMSQL = ?,DIEMTB= ? WHERE MASV = ?";
                    PreparedStatement ps = con.prepareStatement(sql);
                    ps.setString(1, txtDiemJava.getText());
                    ps.setString(2, txtDiemSQL.getText());
                    tinhdiemtb();
                    ps.setString(3, txtDiemTB.getText());
                    ps.setString(4, txtMaSV.getText());
                    ps.executeUpdate();
                    dftb.setRowCount(0);
                    loadData();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Không update được điểm");
                    Logger.getLogger(Connection.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            txtMaSV.enable(false);
            txtDiemJava.enable(false);
            txtDiemSQL.enable(false);
    }//GEN-LAST:event_btLuuActionPerformed


    private void loadData() {

        try {
            Connect a = new Connect();
            Connection con = a.getConnection();
            String sql = "SELECT * FROM Khoa";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            ResultSetMetaData meta = rs.getMetaData();
            int n = meta.getColumnCount();
            Vector col = new Vector();
            col.add("Mã khoa");
            col.add("Tên khoa");
            dftb.setColumnIdentifiers(col);
            while (rs.next()) {
                Vector row = new Vector();
                for (int i = 0; i < n; i++) {
                    if (i < n) {
                        row.addElement(rs.getString(i + 1));
                    } else {
                    }
                }
                dftb.addRow(row);

            }
            tblKhoa.setModel(dftb);
            tblKhoa.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent e) {
                    if (tblKhoa.getSelectedRow() >= 0) {
                        txtMaKhoa.setText(tblKhoa.getValueAt(tblKhoa.getSelectedRow(), 0).toString());
                        txtTenKhoa.setText(tblKhoa.getValueAt(tblKhoa.getSelectedRow(), 1).toString());
                    }
                }
            });
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Lỗi : " + ex);
            Logger.getLogger(Connection.class.getName()).log(Level.SEVERE, null, ex);
        }

    }


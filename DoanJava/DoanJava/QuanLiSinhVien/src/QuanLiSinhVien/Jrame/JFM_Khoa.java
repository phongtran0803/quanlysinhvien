public void getSelectFirst(){
            int selectedRow= 0;
            txtMaKhoa.setText(tblKhoa.getValueAt(selectedRow, 0).toString());
            txtTenKhoa.setText(tblKhoa.getValueAt(selectedRow,1).toString());
            tblKhoa.setRowSelectionInterval(0, 0);
    }
    public void getSelectPrevious(){
        if(tblKhoa.getSelectedRow()>= 1){
            int selectedRow= tblKhoa.getSelectedRow()-1;
            txtMaKhoa.setText(tblKhoa.getValueAt(selectedRow, 0).toString());
            txtTenKhoa.setText(tblKhoa.getValueAt(selectedRow,1).toString());
            tblKhoa.setRowSelectionInterval(selectedRow, selectedRow);
        }
    }
    public void getSelectNext(){
        if(tblKhoa.getSelectedRow()< tblKhoa.getRowCount()-1){
            int selectedRow= tblKhoa.getSelectedRow()+1;
            txtMaKhoa.setText(tblKhoa.getValueAt(selectedRow, 0).toString());
            txtTenKhoa.setText(tblKhoa.getValueAt(selectedRow,1).toString());
            tblKhoa.setRowSelectionInterval(selectedRow, selectedRow);
        }
    }
    public void getSelectLast(){
            int selectedRow= tblKhoa.getRowCount()-1;
            txtMaKhoa.setText(tblKhoa.getValueAt(selectedRow, 0).toString());
            txtTenKhoa.setText(tblKhoa.getValueAt(selectedRow,1).toString());
            tblKhoa.setRowSelectionInterval(0, 0);
    }
